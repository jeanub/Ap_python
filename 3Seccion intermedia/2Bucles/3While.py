"""
El bucle 'while' es una estructura de control que se utiliza para repetir un 
bloque de codigo meintras una condicion se cumpla.
"""

""""
#?While condicon:
    #Bloque de codigo
Donde 'condicion' es una expresion que se evalua como verdadera ('True') o falsa ('False')
mientras la condicion sea verdadera, el bloque de codigo dentro del bucle 'while' se ejecutara
repetidamente. cuando la condicon se evalua como falsa,  la ejecucion del bucle se dentendra y
el control del programa pasara a la siguiente insctruccion del bucle
"""
#?EJEMPLO:
numero = 1
while numero <=5:
    print(numero)
    numero +=1

