"""
En Python, los operadores son simbolos especiales que se utilizan para realizar operaciones
en variables y valores:

!Operadores Aritmeticos:
Suma: + (Suma dos operandos)
Resta: - (Resta el segundo operando del primero)
Multiplicacion: * (Multiplica dos operados)
Division: / (divide el primer operando por el segundo. Siempre produce un resultado tipo "FLOAT")
Division entera: // (Divide el primer operando por el segundo y devuelve un entero)
Modulo: % (devuelve el resto de la divicion del primer operando por el segundo)
Exponenciacion: **  (Eleva el primer operando a la potencia del segundo)

!Operadores de Asignacion:
Asignacion: '=' (asigna el valor del lado derecho al operando del lado izquierdo)
Asignacion compuesto: +=, -=, *=, /=, //=, %=, **=. (Realiza una operacion aritmetica y luego asigna el resultado
a la variable)

!Operadores de Comparacion:
igual a: == (Comprueba si dos valores son iguales)
no igual a: != (Comprueba si dos valores no son iguales)
menor que: < (comprueba si el operando de la izquierda es menor que el de la derecha)
mayor que: > (comprueba si el operando de la izquierda es mayor que el de la derecha)
menor o igual que: <= (Comprueba si el operando de la izquierda es menor o igual al de la derecha)
mayor o igual que: >= (Comprueba si el operando de la izquierda es menor o igual al de la derecha)

!Operadores logicos:
y logico: 'AND' (devuelve "TRUE" si ambos operandos son true)
o logico: 'OR' (devuelve "TRUE" si al menos uno de los operados es true)
no logico: 'NOT' (devuelve "TRUE" si el operando es "FALSE" y "FALSE" si el operando es "TRUE")

!operadores de pertenencia: 
    'IN': devuelve 'True' si un valor esta presente en una secuencia (Por ejemplo: lista, tupla, cadena, etc).

!Operadores de identidad: 
    'IS': devuelve 'True' si dos variables son el mismo objeto.
"""