"""
Los operadores de comparacion se utilizan para comparar dos valores y devolver
un resultado basado en la relacion entre ellos
"""

#?Igualdad (==): Este operador devuelve True si los dos operandos son iguales, y False en caso contrario.
a = 5
b = 5
resultado = a == b
print(resultado)  # Output: True

#?Desigualdad (!=): Este operador devuelve True si los dos operandos no son iguales, y False en caso contrario.
a = 5
b = 3
resultado = a != b
print(resultado)  # Output: True

#?Mayor que (>): Este operador devuelve True si el operando de la izquierda es mayor que el operando de la derecha, y False en caso contrario.
a = 5
b = 3
resultado = a > b
print(resultado)  # Output: True

#?Mayor o igual que (>=): Este operador devuelve True si el operando de la izquierda es mayor o igual que el operando de la derecha, y False en caso contrario.
a = 5
b = 5
resultado = a >= b
print(resultado)  # Output: True

#?Menor que (<): Este operador devuelve True si el operando de la izquierda es menor que el operando de la derecha, y False en caso contrario.
a = 3
b = 5
resultado = a < b
print(resultado)  # Output: True

#?Menor o igual que (<=): Este operador devuelve True si el operando de la izquierda es menor o igual que el operando de la derecha, y False en caso contrario.
a = 5
b = 5
resultado = a <= b
print(resultado)  # Output: True

"""
Estos operadores son fundamentales para realizar comparaciones en python y son ampliamente
utilizados en estructuras de control como 'if', 'while' y 'for'
"""